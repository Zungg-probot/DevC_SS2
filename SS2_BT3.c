#include <stdio.h>

int main() {
    
    int num1 = 10;  
    int num2 = 20;  

    
    int tong = num1 + num2;
    int hieu = num1 - num2;
    int tich = num1 * num2;
    int thuong = num1 / num2;
    printf("Tong cua 2 so nguyen la: %d\n", tong);
    printf("Hieu cua 2 so nguyen la: %d\n", hieu);
    printf("Tich cua 2 so nguyen la: %d\n", tich);
    printf("Thuong cua 2 so nguyen la: %d\n", thuong);
    return 0;
}
